            submitBtn = $("#submitBtn");

            $(document).on('keypress', function (e) {
                if (e.which == 13){
                    return undefined;
                }
            })

            function reloadCaptcha(){
                $.ajax({
                    type: 'POST',
                    url: 'contact-us-form/reload',
                    data: {
                        '_token': "{{ csrf_token() }}"
                    },
                    success: function (data) {
                        $("#captchaSpan").html(data.captcha);
                    }
                });
            }

            $("#reloadCaptchaBtn").click(function (event) {
                event.preventDefault();

                reloadCaptcha();
            });

            submitBtn.click(function ( event ) {
                event.preventDefault();

                $(".alert ul").empty();
                $(".alert").removeClass("alert-danger");

                submitBtn.prop('disabled', true);

                submitBtn.html('<i class="fa fa-spinner fa-spin"></i>');

                $.ajax({
                    type: "POST",
                    url: "contact-us-form/submit",
                    data: {
                        name: $('input[name="name"]').val(),
                        email: $('input[name="email"]').val(),
                        phone: $('input[name="phone"]').val(),
                        subject: $('input[name="subject"]').val(),
                        message: $('textarea[name="message"]').val(),
                        captcha: $('input[name="captcha"]').val(),
                        _token: "{{ csrf_token() }}"
                    },
                    success: function (result) {
                        $(".alert ul").empty();
                        $(".alert").removeClass("alert-danger");

                        $("input[name='captcha']").val("");

                        console.log(result);

                        if (result.status === true){
                            $(".alert").addClass('alert-success');
                            $(".alert ul").append('<li><i class="fas fa-thumbs-up"></i> Thank you for contacting us! We will be in touch with you as soon as possible.</li>')
                        }

                        submitBtn.html("Sent");
                    },
                    error: function(result){
                        $(".alert").addClass('alert-danger');
                        $.each(result.responseJSON.errors, function (i, item) {
                            $(".alert ul").append('<li><i class="fas fa-times"></i>  ' + item[0] + '</li>')
                        })

                        reloadCaptcha();

                        $("input[name='captcha']").val("");

                        $([document.documentElement, document.body]).animate({
                            scrollTop: $("#id7").offset().top
                        }, 500);

                        submitBtn.prop('disabled', false);
                        submitBtn.html('Send Message');
                    },
                    dataType: "json"
                });
            });
